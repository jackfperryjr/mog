        [Authorize(Roles="Admin")]
        public IActionResult Users(string currentFilter, string sortOrder, string searchString, int? page)  
        {  
            ViewData["EmailSort"] = String.IsNullOrEmpty(sortOrder) ? "Email" : "";
            ViewData["CurrentFilter"] = searchString;

            if (searchString != null)
            {
                page = 1;
            }
            else
            {
                searchString = currentFilter;
            }

            var users = (from user in _context.Users  
                        select new  
                        {  
                            UserId = user.Id,                                        
                            FirstName = user.FirstName,  
                            LastName = user.LastName,
                            Picture = user.Picture,
                            Email = user.Email,
                            EmailConfirmed = user.EmailConfirmed 
                        }).ToList().Select(u => new ApplicationUserViewModel()  
                        {  
                            UserId = u.UserId,  
                            FirstName = u.FirstName, 
                            LastName = u.LastName, 
                            Picture = u.Picture,
                            Email = u.Email,
                            EmailConfirmed = u.EmailConfirmed.ToString()  
                        });  

            users = users.OrderBy(u => u.Email);
            if (!String.IsNullOrEmpty(searchString))
            {
                users = users.Where(u => u.Email.Contains(searchString.First().ToString() + searchString.Substring(1))); // Search by user email
            }

            return View(users.ToList());  
        } 

        [HttpGet]
        [Authorize(Roles="Admin")] // Because only I want to register new users
        public IActionResult Register(string returnUrl = null)
        {
            ViewData["ReturnUrl"] = returnUrl;
            return View();
        }

        [HttpPost]
        [Authorize(Roles="Admin")] // Same as above
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Register(RegisterViewModel model, string returnUrl = null)
        {
            ViewData["ReturnUrl"] = returnUrl;
            if (ModelState.IsValid)
            {
                var user = new ApplicationUser { UserName = model.Email, Email = model.Email.ToLower(), FirstName = "New User", Picture = "images/icons/icon-default-profile.jpg" };
                var result = await _userManager.CreateAsync(user, model.Password);
                if (result.Succeeded)
                {
                    _logger.LogInformation("User created a new account with password.");

                    var code = await _userManager.GenerateEmailConfirmationTokenAsync(user);
                    var callbackUrl = Url.EmailConfirmationLink(user.Id, code, Request.Scheme);
                    await _emailSender.SendEmailConfirmationAsync(model.Email, callbackUrl);
                    await _emailSender.SendRegistrationEmailToAdminAsync(model.Email);

                    //await _signInManager.SignInAsync(user, isPersistent: false);

                    if (User.IsInRole("Admin")) 
                    {
                        return RedirectToAction(nameof(Users));
                    }
                    else 
                    {
                        return RedirectToAction(nameof(MonsterController.Index), "Monster"); // Just in case I open up registration.
                    }
                }
                AddErrors(result);
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

        [Authorize(Roles="Admin")]
        public async Task<IActionResult> Delete(string id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var user = await _context.Users.SingleOrDefaultAsync(u => u.Id == id);
            _context.Users.Remove(user);
            await _context.SaveChangesAsync();
            return RedirectToAction(nameof(Users));
        }

